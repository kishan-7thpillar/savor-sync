// Helper function to calculate shift duration in hours
export function calculateShiftHours(startAt: string, endAt: string): number {
  const start = new Date(startAt);
  const end = new Date(endAt);
  return (end.getTime() - start.getTime()) / (1000 * 60 * 60);
}

// Helper function to calculate shift cost
export function calculateShiftCost(shift: Shift): number {
  const hours = calculateShiftHours(shift.start_at, shift.end_at);
  const hourlyRate = shift.wage.hourly_rate.amount / 100; // Convert from cents
  return hours * hourlyRate;
}

// Helper function to format currency
export function formatCurrency(amount: number, currency: string = 'USD'): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: currency,
  }).format(amount);
}
